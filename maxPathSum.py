import math


def maxPathSum(grid):
    def helper(g, r, c, memo):
        post = str(r) + "," + str(c)
        if r == len(g) or c == len(g[0]):
            return -math.inf
        if r == len(g) - 1 and c == len(g[0]) - 1:
            return grid[r][c]
        if post in memo:
            return memo[post]
        downCount = helper(g, r + 1, c, memo)
        rightCount = helper(g, r, c + 1, memo)
        memo[post] = g[r][c]+ max(downCount , rightCount)
        return memo[post]

    return helper(grid, 0, 0, {})


